package com.terminal3.gpcoreui.enums;

/**
 * Defines the type of input option that can be rendered dynamically.
 */
public enum GPOptionType {
    /** Simple EPIN input field */
    EPIN,
    /** Simple text input field */
    INPUT_FIELD,
    /** Dropdown selection list */
    DROPDOWN,
    /** Redirect action */
    REDIRECT,
    /** Group of options linked to a dropdown */
    GROUP
}
